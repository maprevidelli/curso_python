# type convertion, typecasting, coercion
# converter um tipo em outro
# tipos imutáveis e primitivos: str, int, float, bool
print(int('1'), type(int('1'))) # converteu uma str '1' para inteiro
print(int('1') + 1) # uma str '1' convertina para int pode ser somado com 1 q é int
print(type(float('1') + 1.1))
