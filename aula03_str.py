"""
Python
Tipo de tipagem = Dinamica / forte
str -> String -> texto
Strings são textos que estão dentro de aspas
"""
# Aspas simples
print('Marco Antonio')
# Aspas duplas
print("Marco Antonio")

# Escape
print('Marco \'Antonio\'') #motras as aspas

# r
print(r'Marco \'Antonio\'') # mostra as barras

# jeito mais Limpo
print('Marco "Antonio"') # inverte a simples com a dupla conforme quer exibir
